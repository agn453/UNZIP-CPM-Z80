UNZIP Undeflates, Unreduces, Unshrinks, Unimplodes and Unstores, files 
from MS-DOS ZIP files.  Accepts DU and DIR specs for ZIP file and as 
destination for extracted files. Output filenames are supported.  Can be 
aborted with ^C without leaving partial files on disk.  
Version 1.82 corrects some bugs and adds undeflate.
